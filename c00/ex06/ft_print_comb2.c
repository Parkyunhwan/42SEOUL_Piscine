#include <unistd.h>

void		ft_print_comb2(void)
{
	char numbers[5];
	int i;

	i = 0;
	while (i < 10000)
	{
		numbers[0] = i / 1000 + '0';
		numbers[1] = (i % 1000) / 100 + '0';
		numbers[2] = ' ';
		numbers[3] = (i % 1000) % 100 / 10 + '0';
		numbers[4] = (i % 1000) % 100 % 10 + '0';
		if ((i / 100) < (i % 100))
		{
			write(1, &numbers, 5);
			if (!((i / 100 == 98) && (i % 100 == 99)))
				write(1, ", ", 2);
		}
		i++;
	}
}
